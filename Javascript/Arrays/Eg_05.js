// shift and unshift

// shift removing the element
// removes from starting 

let names = ["A","B","C","D","E"]
console.log(names.shift())
console.log(names.shift())
console.log(names)

// unshift  adding the element
// adding from starting

let animals = ["A","B","C","D","E"]
console.log(animals.unshift("cat"))
console.log(animals)