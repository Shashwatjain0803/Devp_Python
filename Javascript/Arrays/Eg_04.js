// push  and  pop
// push // adding it from last
let names = ["A","B","C","D","E"]
console.log(names)
console.log(names.length)
console.log(names.push("G"))
console.log(names.push("F"))
console.log(names)
console.log(names.length)

// pop method   // removes from last
let colors = ["A","B","C","D","E"]
console.log(colors)
console.log(colors.pop())
console.log(colors.pop())
console.log(colors)